package pojoconcepts;

public class FinData {

	private String accNum;
	private long salary;
	private String pfNum;
	private long pf;
	
	public String getAccNum() {
		return accNum;
	}
	public void setAccNum(String accNum) {
		this.accNum = accNum;
	}
	public long getSalary() {
		return salary;
	}
	public void setSalary(long salary) {
		this.salary = salary;
	}
	public String getPfNum() {
		return pfNum;
	}
	public void setPfNum(String pfNum) {
		this.pfNum = pfNum;
	}
	public long getPf() {
		return pf;
	}
	public void setPf(long pf) {
		this.pf = pf;
	}
	
	
	
}
